    private void imgBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imgBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = victimTbl.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row to delete the account", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            DisasterVictim dv = (DisasterVictim) victimTbl.getValueAt(selectedRow, 0);
            Icon img = dv.getImage();
            imgLbl.setIcon(img);
        
        try {
             File file = dv.getFile();
             BufferedImage image = ImageIO.read(new File(file.getAbsolutePath()));
             ImageIcon image1=new ImageIcon(scaleImage(314, 288, image));//get the image from file chooser and scale it to match JLabel size
             imgLbl.setIcon(image1);
            } catch (Exception ex) {}              
        }
    }//GEN-LAST:event_imgBtnActionPerformed

