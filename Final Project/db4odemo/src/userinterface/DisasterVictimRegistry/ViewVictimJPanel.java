    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) victimTbl.getModel();
        model.setRowCount(0);
                
        if(victimCombo.getItemCount()>0){
            if(victimCombo.getSelectedItem().equals("Victim")){
            for(DisasterVictim dv : organization.getDisasterVictimDirectory().getVictimList()) {
                if(dv.getType().equals("Victim")){
                    Object[] row = new Object[model.getColumnCount()];
                    row[0] = dv;
                    row[1] = dv.getAge();
                    row[2] = dv.getGender();
                    row[3] = dv.getAddress();
                    row[4] = dv.getContact();
                    row[5] = dv.getNationality();
                    row[6] = dv.getType();
                    model.addRow(row);
                }
        }
            }else if(victimCombo.getSelectedItem().equals("Missing Person")){
            for(DisasterVictim dv : organization.getDisasterVictimDirectory().getVictimList()) {
                if(dv.getType().equals("Missing Person")){
                    Object[] row = new Object[model.getColumnCount()];
                    row[0] = dv;
                    row[1] = dv.getAge();
                    row[2] = dv.getGender();
                    row[3] = dv.getAddress();
                    row[4] = dv.getContact();
                    row[5] = dv.getNationality();
                    row[6] = dv.getType();
                    model.addRow(row);
                } 
        } 
        } 
        }          
    }
